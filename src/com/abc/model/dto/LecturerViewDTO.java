package com.abc.model.dto;

/**
 *
 * @author nipun
 */
public class LecturerViewDTO {
    
    private final int id;
    private final String name;
    private final String mobile;
    private final String email;

    public LecturerViewDTO(int id, String name, String mobile, String email) {
        this.id = id;
        this.name = name;
        this.mobile = mobile;
        this.email = email;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getMobile() {
        return mobile;
    }

    public String getEmail() {
        return email;
    }
}
